const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider = ({ children }: { children: ReactNode }) => {
  const [language, setLanguageState] = useState<Language>(() => readStoredLanguage());
  const [userId, setUserId] = useState<string | null>(null);

  const applyLanguage = (nextLanguage: Language) => {
    setLanguageState(nextLanguage);
    writeStoredLanguage(nextLanguage);
  };

  const persistLanguagePreference = useCallback(async (nextLanguage: Language, nextUserId = userId) => {
    if (!nextUserId) {
      return;
    }

    const { error } = await supabase.rpc("set_user_language_preference", {
      p_language: nextLanguage,
    });

    if (error) {
      console.error("Failed to persist language preference:", error.message);
    }
  }, [userId]);

  const syncLanguageFromProfile = useCallback(async (nextUserId: string) => {
    const { data, error } = await supabase
      .from("profiles")
      .select("preferred_language")
      .eq("user_id", nextUserId)
      .maybeSingle();

    if (error) {
      console.error("Failed to load language preference:", error.message);
      return;
    }

    if (isLanguage(data?.preferred_language)) {
      applyLanguage(data.preferred_language);
      return;
    }

    void persistLanguagePreference(readStoredLanguage(), nextUserId);
  }, [persistLanguagePreference]);

  useEffect(() => {
    const handleSession = (nextSessionUserId: string | null) => {
      setUserId(nextSessionUserId);

      if (nextSessionUserId) {
        void syncLanguageFromProfile(nextSessionUserId);
        return;
      }

      applyLanguage(readStoredLanguage());
    };

    void supabase.auth.getSession().then(({ data: { session } }) => {
      handleSession(session?.user?.id ?? null);
    });

    const {
      data: { subscription },
    } = supabase.auth.onAuthStateChange((_event, session) => {
      handleSession(session?.user?.id ?? null);
    });

    return () => {
      subscription.unsubscribe();
    };
  }, [syncLanguageFromProfile]);

  const setLanguage = (nextLanguage: Language) => {
    applyLanguage(nextLanguage);
    void persistLanguagePreference(nextLanguage);
  };

  const t = (key: string, params?: TranslationParams): string => {
    const template =
      extraTranslations[language]?.[key] ??
      translations[language]?.[key] ??
      extraTranslations[DEFAULT_LANGUAGE]?.[key] ??
      translations[DEFAULT_LANGUAGE]?.[key] ??
      key;

    return formatTranslation(template, params);
  };

  return (
    <LanguageContext.Provider
      value={{
        language,
        setLanguage,
        languageShortLabel: getLanguageShortLabel(language),
        languageOptions: LANGUAGE_OPTIONS,
        t,
      }}
    >
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within LanguageProvider");
  }
  return context;
};

